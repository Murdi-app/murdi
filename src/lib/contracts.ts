// نصوص عقود الخدمتين الخاصتين (تمويل/استثمار) + دالة توليد المسودّة بالحقول

export interface ContractFields {
  clientName?: string;
  clientIdNumber?: string;
  establishmentName?: string;
  establishmentCr?: string;
  feePercent?: string | number;
}

const F = (v: string | number | undefined, fallback: string) =>
  (v === undefined || v === null || v === '') ? fallback : String(v);

export function fundingContract(f: ContractFields): string {
  return `عقد تقديم خدمة تجهيز ملف تمويلي

أُبرم هذا العقد بعون الله وتوفيقه بين كلٍّ من:

الطرف الأول: شركة حلول المرضي للاستشارات المالية، ويمثّلها المستشار/ عبدالحكيم العتيبي، رقم الهوية (1101036539). ويُشار إليها بـ «الطرف الأول».

الطرف الثاني: السيد/ة ${F(f.clientName, '(..............)')}، رقم الهوية ${F(f.clientIdNumber, '(..............)')}، بصفته مالك/ممثل منشأة ${F(f.establishmentName, '(..............)')}، سجل تجاري رقم ${F(f.establishmentCr, '(..............)')}. ويُشار إليه بـ «الطرف الثاني».

وقد أقرّ الطرفان بكامل أهليتهما الشرعية والنظامية، واتفقا على ما يلي:

تمهيد
يرغب الطرف الثاني في تجهيز ملفه التمويلي ورفع جاهزيته للحصول على تمويل من الجهات المناسبة، ولِما يتمتّع به الطرف الأول من خبرة في الاستشارات المالية وتجهيز الملفات، اتفق الطرفان على تنظيم هذه الخدمة وفق البنود الآتية.

البند الأول: موضوع العقد
يقدّم الطرف الأول للطرف الثاني خدمة تجهيز الملف التمويلي ورفع الجاهزية، وتشمل على سبيل المثال لا الحصر:
- دراسة الحالة المالية للطرف الثاني وتشخيص جاهزيته التمويلية.
- مراجعة المستندات وتجميع المتطلبات اللازمة وتنظيمها في ملف متكامل.
- معالجة الفجوات ورفع جاهزية الملف بما يتوافق مع متطلبات الجهات التمويلية.
- إعداد ملف غرض التمويل بصورة احترافية تُبرز قوة المركز المالي.
- تقديم الاستشارات والإرشاد حول الجهات المناسبة وآلية التقديم.
- المتابعة والإرشاد حتى اكتمال جاهزية الملف وصدور القرار التمويلي.

البند الثاني: الأتعاب
يلتزم الطرف الثاني بسداد أتعاب للطرف الأول مقابل خدمة تجهيز الملف ورفع الجاهزية قدرها نسبة (${F(f.feePercent, '....')}٪) من قيمة التمويل الذي يحصل عليه الطرف الثاني.
وتستحق هذه الأتعاب عند: صرف مبلغ التمويل للطرف الثاني، أو تمكينه من الاستفادة من التمويل بأي وسيلة كانت.

البند الثالث: بدء التنفيذ
يُعدّ توقيع هذا العقد، وقيام الطرف الثاني بأيٍّ من الإجراءات الآتية، بدءاً فعلياً للخدمة: تسليم المستندات المطلوبة لتجهيز الملف، أو تفويض الطرف الأول بتجهيز الملف ومتابعته، أو اعتماد الملف المُجهّز لتقديمه لأي جهة تمويلية.

البند الرابع: حفظ حق الطرف الأول وعدم الالتفاف
يقر الطرف الثاني بأن جهد الطرف الأول في تجهيز الملف ورفع الجاهزية والإرشاد إلى الجهات المناسبة هو السبب المباشر في تأهّله للتمويل.
وعليه، فإن أي تمويل يحصل عليه الطرف الثاني من أي جهة جرى تجهيز ملفه لها أو الإرشاد إليها من الطرف الأول، خلال مدة اثني عشر شهراً من تاريخ تجهيز الملف أو الإرشاد، يُعدّ ثمرةً مباشرةً لجهد الطرف الأول وتستحق عنه الأتعاب كاملة.
ولا يجوز للطرف الثاني تجاوز الطرف الأول أو إيقاف الإجراء ثم العودة للجهة ذاتها — مباشرةً أو عبر وسيط — بقصد تفادي سداد الأتعاب. وفي حال ثبوت ذلك، يحق للطرف الأول المطالبة بالأتعاب كاملة، إضافةً إلى تعويض اتفاقي قدره (50٪) منها مقابل الجهد المبذول، ما لم تُقدّر الجهة المختصة خلاف ذلك.

البند الخامس: السند التنفيذي
يوافق الطرف الثاني على إصدار سند لأمر إلكتروني عبر منصة (نافذ) ضماناً لسداد الأتعاب المستحقة، ويحق للطرف الأول التقدّم به مباشرةً إلى محكمة التنفيذ عند الإخلال بالسداد.

البند السادس: التزامات الطرف الثاني وإقراراته
يلتزم الطرف الثاني بتقديم معلومات صحيحة وكاملة، والتعاون في توفير المستندات المطلوبة.
ويقرّ الطرف الثاني بأن الطرف الأول لا يضمن الموافقة على التمويل، وإنما يقدّم خدمة تجهيز الملف ورفع الجاهزية والإرشاد، وأن القرار النهائي يعود للجهة التمويلية.

البند السابع: النظام المطبّق
يخضع هذا العقد لأنظمة المملكة العربية السعودية، ويُفصل في أي نزاع وفق الأنظمة المعمول بها.

البند الثامن: التوقيع
يقرّ الطرفان بأن هذا العقد مُلزِمٌ ومنتِج لكامل آثاره النظامية بمجرّد توقيعه منهما. ويكون التوقيع صحيحاً سواءً تم بالتوقيع الخطي المصوّر، أو باعتماد العقد ورفع النسخة الموقّعة عبر منصة مُرضي الإلكترونية، أو بتبادله عبر الوسائل الإلكترونية المعتمدة بين الطرفين (كالبريد الإلكتروني أو تطبيق واتساب)، أو عبر منصات التوقيع الإلكتروني المعتمدة. ويُعدّ اعتماد العقد أو تبادل نسخته الموقّعة عبر أيٍّ من هذه الوسائل بمثابة تبادل الأصل، ويأخذ حكم التوقيع الخطي نظاماً، ويُحتجّ به بين الطرفين وأمام الجهات المختصة. وحُرِر العقد من نسختين، بيد كل طرف نسخة.

الطرف الأول: المستشار/ عبدالحكيم العتيبي — حلول المرضي للاستشارات المالية
التوقيع: عبدالحكيم العتيبي

الطرف الثاني: ${F(f.clientName, '..............')}
التوقيع: ............`;
}

